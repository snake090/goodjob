package com.goodjob.dto;

import java.sql.Date;
/**
 *
 * @author 弘中翔太郎
 * @version 1.0
 *
 */
public class Item {

	private static final long serialVersionUID = 1L;
	private int item_number;
	private String item_name;
	private int unit_price;
	private String colors;
	private String patterns;
	private String sizes;
	private Date  expiration_date;
	/**
	 * 
	 * @return item_number int
	 */
	public int getItem_number() {
		return item_number;
	}
	/**
	 * 
	 * @param item_number int
	 */
	public void setItem_number(int item_number) {
		this.item_number = item_number;
	}
	/**
	 * 
	 * @return item_name String
	 */
	public String getItem_name() {
		return item_name;
	}
	/**
	 * 
	 * @param item_name String
	 */
	public void setItem_name(String item_name) {
		this.item_name = item_name;
	}
	/**
	 * 
	 * @return unit_price int
	 */
	public int getUnit_price() {
		return unit_price;
	}
	/**
	 * 
	 * @param unit_price int
	 */
	public void setUnit_price(int unit_price) {
		this.unit_price = unit_price;
	}
	/**
	 * 
	 * @return colors String 
	 */
	public String getColors() {
		return colors;
	}
	/**
	 * 
	 * @param colors String
	 */
	public void setColors(String colors) {
		this.colors = colors;
	}
	/**
	 * 
	 * @return patterns String
	 */
	public String getPatterns() {
		return patterns;
	}
	/**
	 * 
	 * @param patterns String
	 */
	public void setPatterns(String patterns) {
		this.patterns = patterns;
	}
	/**
	 * 
	 * @return sizes String
	 */
	public String getSizes() {
		return sizes;
	}
	/**
	 * 
	 * @param sizes String
	 */
	public void setSizes(String sizes) {
		this.sizes = sizes;
	}
	/**
	 * 
	 * @return expiration_date Date
	 */
	public Date getExpiration_date() {
		return expiration_date;
	}
	/**
	 * 
	 * @param expiration_date Date
	 */
	public void setExpiration_date(Date expiration_date) {
		this.expiration_date = expiration_date;
	}




}
